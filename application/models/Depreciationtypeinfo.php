    public function Depreciationtypeedit(){
        $recordID=$this->input->post('recordID');

        $this->db->select('*');
        $this->db->from('tbl_depreciation_type');
        $this->db->where('idtbl_depreciation_type', $recordID);
        $this->db->where('status', 1);

        $respond=$this->db->get();

        $obj=new stdClass();
        $obj->id=$respond->row(0)->idtbl_depreciation_type;
        $obj->depreciation_type=$respond->row(0)->depreciation_type;
       

        echo json_encode($obj);
    }
