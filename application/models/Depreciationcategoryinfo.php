    public function Depreciationcategoryedit(){
        $recordID=$this->input->post('recordID');

        $this->db->select('*');
        $this->db->from('tbl_depreciation_category');
        $this->db->where('idtbl_depreciation_category', $recordID);
        $this->db->where('status', 1);

        $respond=$this->db->get();

        $obj=new stdClass();
        $obj->id=$respond->row(0)->idtbl_depreciation_category;
        $obj->depreciation_category=$respond->row(0)->depreciation_category;
        

        echo json_encode($obj);
    }
