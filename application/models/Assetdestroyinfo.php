    public function Assetdestroyedit(){
        $recordID=$this->input->post('recordID');

        $this->db->select('*');
        $this->db->from('tbl_asset_destroy');
        $this->db->where('idtbl_asset_destroy', $recordID);
        $this->db->where('status', 1);

        $respond=$this->db->get();

        $obj=new stdClass();
        $obj->id=$respond->row(0)->idtbl_asset_destroy;
        $obj->destroy_item=$respond->row(0)->destroy_item;
        $obj->destroy_date=$respond->row(0)->destroy_date;
        $obj->destroy_amount=$respond->row(0)->destroy_amount;
        $obj->destroy_reason=$respond->row(0)->destroy_reason;

        echo json_encode($obj);
    }
